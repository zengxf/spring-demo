DROP TABLE IF EXISTS `user1`;

CREATE TABLE `user1` (
	`id` BIGINT(19) NOT NULL AUTO_INCREMENT,
	`age` INT(10) NULL DEFAULT NULL,
	`status` INT(10) NULL DEFAULT NULL,
	`name` VARCHAR(255) NULL DEFAULT NULL COLLATE 'utf8mb4_bin',
	`login_mobile` VARCHAR(255) NULL DEFAULT NULL COLLATE 'utf8mb4_bin',
	PRIMARY KEY (`id`) USING BTREE
) COLLATE='utf8mb4_bin' ENGINE=InnoDB;

DROP TABLE IF EXISTS `json_data`;
CREATE TABLE `json_data` (
	`id` INT NOT NULL AUTO_INCREMENT,
	`remark` VARCHAR(50) NULL DEFAULT NULL,
	`user_ids` JSON NULL COMMENT '格式如：[1,2,3]',
	`create_date` DATETIME NULL DEFAULT CURRENT_TIMESTAMP(),
	PRIMARY KEY (`id`)
)COLLATE='utf8mb4_bin' ENGINE=INNODB COMMENT='JSON 数据测试';
