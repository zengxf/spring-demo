CREATE TABLE `im_msg` (
	`id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT,
	`from_uid` BIGINT(20) UNSIGNED NOT NULL DEFAULT '0',
	`to_uid` BIGINT(20) UNSIGNED NOT NULL DEFAULT '0',
	`msg_type` INT(10) NOT NULL DEFAULT '1',
	`msg_body` VARCHAR(500) NULL DEFAULT NULL COLLATE 'utf8mb4_bin',
	`msg_status` INT(10) NOT NULL DEFAULT '1',
	`send_time` DATETIME(3) NULL DEFAULT NULL,
	`read_time` DATETIME(3) NULL DEFAULT NULL,
	PRIMARY KEY (`id`) USING BTREE,
	INDEX `to_uid` (`to_uid`) USING BTREE
)
COLLATE='utf8mb4_bin'
ENGINE=InnoDB
AUTO_INCREMENT=10103
;
